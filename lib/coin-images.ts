/** Get a coin image URL by symbol with a ui-avatars.com fallback */
export function getCoinImage(symbol: string): string {
  return COIN_IMAGES[symbol] || ""
}

/** Generate a fallback avatar URL for a given symbol */
export function coinFallback(symbol: string): string {
  return `https://ui-avatars.com/api/?name=${encodeURIComponent(symbol)}&size=64&background=random&bold=true&color=fff`
}

/** onError handler for coin image elements */
export function handleCoinImageError(e: React.SyntheticEvent<HTMLImageElement>, symbol: string) {
  ;(e.target as HTMLImageElement).src = coinFallback(symbol)
}
